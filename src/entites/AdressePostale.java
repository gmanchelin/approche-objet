package entites;

public class AdressePostale {
	int numRue;
	String libelleRue;
	int codePostal;
	String ville;
	
	
	public AdressePostale(int numRue, String libelleRue, int codePostal, String ville) {
		numRue = this.numRue;
		libelleRue = this.libelleRue;
		codePostal = this.codePostal;
		ville = this.ville;
	}
}
